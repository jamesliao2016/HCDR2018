# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution(object):
    def isPalindrome(self, head):
        """
        :type head: ListNode
        :rtype: bool
        """
        tmp = []
        while head:
            tmp.append(head.val)
            head = head.next
        return True if tmp == tmp[::-1] else False

